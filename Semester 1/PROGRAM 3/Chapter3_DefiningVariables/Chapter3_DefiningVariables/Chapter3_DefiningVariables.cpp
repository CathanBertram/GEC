#include <iostream>

using namespace std;


int main() {
	int myInt1 = 5, myInt2;
	myInt2 = 10;
	cout << "int1 =" << myInt1 << endl;
	cout << "int2 =" << myInt2 << endl;

	float myFloat1 = 1.2f, myFloat2;
	myFloat2 = 9.9f;
	cout << "float 1 =" << myFloat1 << endl;
	cout << "float 2 =" << myFloat2 << endl;
	
	char myChar = 'a';
	cout << "char =" << myChar << endl;
	cin.get();
	return 0;


}